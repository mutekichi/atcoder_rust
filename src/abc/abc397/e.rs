fn dfs(
    v: usize,
    from: usize,
    graph: &Vec<Vec<usize>>,
    k: usize,
) -> Option<usize> {
    let mut middles = vec![];
    for &nv in &graph[v] {
        if nv != from {
            if let Some(result) = dfs(nv, v, graph, k) {
                if result == INF_USIZE {
                    continue;
                } else {
                    middles.push(result);
                }
            } else {
                md!(v, from, "here");
                return None;
            }
        }
    }
    if middles.len() == 0 {
        md!(v, from, "len = 0");
        return Some(0);
    } else if middles.len() == 1 {
        if middles[0] == k - 2 {
            md!(v, from, "len = 1");
            return Some(INF_USIZE);
        } else {
            md!(v, from, middles[0] + 1);
            return Some(middles[0] + 1);
        }
    } else if middles.len() == 2 {
        if middles[0] + middles[1] + 3 == k {
            md!(v, from, "len = 2");
            return Some(INF_USIZE);
        } else {
            md!(v, from, "len = 2, NONE");
            return None;
        }
    } else {
        md!(v, from, "len > 2");
        return None;
    }
}

#[allow(unused_variables)]
fn solve<W: Write>(out: &mut W) {
    macro_rules! wl {
        ($x:expr) => { writeln!(out, "{}", $x).unwrap(); };
        ($($arg:tt)*) => { writeln!(out, $($arg)*).unwrap(); };
    }

    input! {
        n: usize,
        k: usize,
        UV: [(Usize1, Usize1); n * k - 1],
    }
    if k == 1 {
        wl!("Yes");
        return;
    }
    let mut tree = vec![vec![]; n * k];
    for (u, v) in UV {
        tree[u].push(v);
        tree[v].push(u);
    }
    if dfs(0, INF_USIZE, &tree, k).is_some() {
        wl!("Yes");
    } else {
        wl!("No");
    }
}

