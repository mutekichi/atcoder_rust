#[macro_export]
#[cfg(debug_assertions)] // for debug build
macro_rules! md { // stands for my_dbg
    ($($arg:expr),* $(,)?) => {{
        eprint!("[{}:{}] ", file!(), line!());

        let mut _first = true;
        $(
            if !_first {
                eprint!(", ");
            }
            eprint!("{}: {}", stringify!($arg), $arg);
            _first = false;
        )*
        eprintln!();
    }};
}

#[macro_export]
#[cfg(not(debug_assertions))] // for release build
macro_rules! md {
    ($($arg:expr),* $(,)?) => {{
        // do nothing
    }};
}

#[allow(unused_variables)]
fn main() {
    input! {
        n: usize, 
        m: usize,
        mut A: [i64; n],
        mut B: [i64; m],
    }
    let mut count = 0usize;
    A.sort_unstable();
    B.sort_unstable();

    let mut j = 0;
    for i in 0..n {
        let a = A[i];
        if a * 2 < B[j] {
            continue;
        }
        else {
            count += 1;
            j += 1;
            if j == m {
                break;
            }
        }
    }
    println!("{}", count);
}

