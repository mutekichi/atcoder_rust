#[allow(unused_macros)]
#[allow(unused_variables)]
#[rustfmt::skip]
fn solve<W: Write>(out: &mut W) {
    macro_rules! wl {
        ($x:expr) => { writeln!(out, "{}", $x).unwrap(); };
        ($($arg:tt)*) => { writeln!(out, $($arg)*).unwrap(); };
    }

    input! {
        t: usize,
        m: i64,
    }
    
    let mut ct = vec![vec![0 as i64; 5003]; 5003];
    for i in 0..5003 {
        ct[i][0] = 1;
    }
    for i in 1..5002 {
        for j in 1..=i {
            ct[i][j] = (ct[i - 1][j - 1] + ct[i - 1][j]) % m;
        }
    }
    md!(ct[8][2]);
    for _ in 0..t {
        input! {
            n: usize,
            vec_c: [i64; n],
        }
        let mut ans = 1 as i64;
        let mut sum = vec_c.iter().sum::<i64>();
        for c in vec_c {
            ans *= ct[sum as usize][c as usize];
            ans %= m;
            sum -= c;
        }
        wl!(ans);
    }
}

