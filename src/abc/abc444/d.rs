fn main() {
    let stdout = stdout();
    let mut out = BufWriter::new(stdout.lock());

    solve(&mut out);

    out.flush().unwrap();
}

#[allow(unused_variables)]
fn solve<W: Write>(out: &mut W) {
    input! {
        n: usize,
        A: [usize; n],
    }
    let mut counts = vec![0; 1000010];
    for a in A {
        counts[0] += 1;
        counts[a] -= 1;
    }
    for i in 0..1000000 {
        counts[i + 1] += counts[i];
    }
    for i in 0..1000000 {
        counts[i + 1] += counts[i] / 10;
        counts[i] %= 10;
    }
    let max_index = {
        let mut ans = 0;
        for i in ( 0..1000000 ).rev() {
            if counts[i] != 0 {
                ans = i;
                break;
            }
        }
        ans
    };
    println!("{}", counts.iter().take(max_index + 1).rev().join(""));

}

