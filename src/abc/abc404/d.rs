#[allow(unused_variables)]
fn solve<W: Write>(out: &mut W) {
    macro_rules! wl {
        ($x:expr) => { writeln!(out, "{}", $x).unwrap(); };
        ($($arg:tt)*) => { writeln!(out, $($arg)*).unwrap(); };
    }

    input! {
        n: usize,
        m: usize,
        C: [i64; n],
    }
    let mut A = vec![vec![]; n];
    for i in 0..m {
        input! {
            k: usize,  
            AA: [Usize1; k],
        }
        for aa in AA {
            A[aa].push(i);
        }
    }

    let mut ans = INF_I64;
    for i in 0..(3usize.pow(n as u32)) {
        mepw!(i);
        let mut counts = vec![0; m];
        let mut current = i;
        let mut cost = 0;
        for j in 0..n {
            md!(current);
            let t = current % 3;
            mepw!(t);
            if t == 0 {
                
            }
            else if t == 1 {
                for &a in &A[j] {
                    counts[a] += 1;
                } 
                cost += C[j];
            }
            else {
                for &a in &A[j] {
                    counts[a] += 2;
                }
                cost += C[j] * 2;
            }
            current /= 3;
        }
        mep!("\n");
        if counts.iter().all(|c| *c > 1) {
            ans = min(ans, cost);
        }
    }
    wl!(ans);
    
}

