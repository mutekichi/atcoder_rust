#[macro_export]
#[cfg(debug_assertions)] // for debug build
macro_rules! md { // stands for my_dbg
    ($($arg:expr),* $(,)?) => {{
        eprint!("[{}:{}] ", file!(), line!());

        let mut _first = true;
        $(
            if !_first {
                eprint!(", ");
            }
            eprint!("{}: {}", stringify!($arg), $arg);
            _first = false;
        )*
        eprintln!();
    }};
}

#[macro_export]
#[cfg(not(debug_assertions))] // for release build
macro_rules! md {
    ($($arg:expr),* $(,)?) => {{
        // do nothing
    }};
}

#[allow(unused_variables)]
fn main() {
    input! {
        n: usize,
        mut A: [i64; n],
    }
    A.sort_unstable();
    let mut ans = A.iter().sum::<i64>() * (n as i64 - 1);
    let mut idx = n - 1;
    for i in 0..n - 1 {
        let a = A[i];
        while a + A[idx] >= 100_000_000 {
            if idx == 0 {
                break;
            }
            idx -= 1;
        }
        let cnt = n - max(i + 1, idx + 1);
        md!(idx, cnt);
        ans -= 100_000_000 * cnt as i64;
    }

    println!("{}", ans);
}

