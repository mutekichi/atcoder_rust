#[allow(unused_variables)]
fn solve<W: Write>(out: &mut W) {
    macro_rules! wl {
        ($x:expr) => { writeln!(out, "{}", $x).unwrap(); };
        ($($arg:tt)*) => { writeln!(out, $($arg)*).unwrap(); };
    }

    input! {
        n: usize,
        m: usize,
        mut A: [i64; n],
        mut B: [i64; m],
    }
    A.push(INF_I64);
    B.push(INF_I64);
    let mut index_set: BTreeMap<i64, BTreeSet<usize>> = BTreeMap::new();
    let mut b_set = BTreeSet::new();
    for &b in &B {
        b_set.insert(b);
    }
    for i in 0..A.len() {
        let a = A[i];
        if b_set.contains(&a) {
            index_set.entry(a).or_default().insert(i);
        }
    }
    let mut greedy = vec![];
    let mut current_index = 0;
    for i in 0..A.len() {
        md!(i, A[i]);
        if A[i] == B[current_index] {
            greedy.push(i);
            current_index += 1;
        }
    }
    if greedy.len() != m + 1 {
        wl!("No");
        return;
    }
    for i in (0..m).rev() {
        let key = B[i];
        let set = index_set.get(&key).unwrap();
        let greedy_idx = greedy[i];
        let greedy_next = greedy[i + 1];
        if set.range((greedy_idx + 1)..greedy_next).count() > 0 {
            wl!("Yes");
            return;
        }
    }
    wl!("No");
}

